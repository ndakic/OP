package zadaci;

public class Exchange {
	
	private int din;
	private int dolar;

	public int getDin() {
		return din;
	}

	public void setDin(int din) {
		this.din = din;
	}
	

	public int getDolar() {
		return dolar;
	}

	public void setDolar(int dolar) {
		this.dolar = dolar;
	}

	@Override
	public String toString() {
		return "Exchange [din=" + din + ", dolar=" + dolar + "]";
	}


	
	

}
